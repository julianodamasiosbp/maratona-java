package maratonajava;

public class Aula04Operadores {

	public static void main(String[] args) {
		int n1 = 10;
		int n2 = 20;
		double resultado = n1 / n2;
		
		System.out.println(n1 + n2);
		System.out.println(n1 - n2);
		System.out.println(n1 * n2);
		System.out.println(n1 / n2);
		System.out.println(resultado);
		System.out.println("Valor: " + n1 + n2);
		System.out.println("Valor: " + (n1 + n2));

	}

}
